package server;

/**
 * The enum State server.
 */
public enum StateServer {
    /**
     * Init state server.
     */
    INIT,
    /**
     * Initialized state server.
     */
    INITIALIZED,
    /**
     * Starting state server.
     */
    STARTING,
    /**
     * Started state server.
     */
    STARTED,
    /**
     * Interrupted state server.
     */
    INTERRUPTED,
    /**
     * Shutdown state server.
     */
    SHUTDOWN,
    FINISHED

}
