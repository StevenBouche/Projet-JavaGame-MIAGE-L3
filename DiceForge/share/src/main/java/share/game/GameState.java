package share.game;

/**
 * The enum Game state.
 */
public enum GameState {
    /**
     * Init game state.
     */
    INIT,
    /**
     * Waiting share.player game state.
     */
    WAITING_PLAYER,
    /**
     * Start game state.
     */
    START,
    /**
     * Stop game state.
     */
    STOP,
    /**
     * Finish game state.
     */
    FINISH,

    RESET,
    INTERRUPT

}
